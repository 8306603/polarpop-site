let currentLanguage = "en";
let currentView = "home";

function getInitialLanguage() {
  const url = new URL(window.location.href);
  const requested = url.searchParams.get("lang");
  if (translations[requested]) return requested;
  const stored = localStorage.getItem("polarpop-language");
  if (translations[stored]) return stored;
  const browser = navigator.language.toLowerCase();
  if (browser.startsWith("zh-tw") || browser.startsWith("zh-hk") || browser.startsWith("zh-mo")) return "zhHant";
  if (browser.startsWith("zh")) return "zhHans";
  if (browser.startsWith("ja")) return "ja";
  if (browser.startsWith("ko")) return "ko";
  if (browser.startsWith("fr")) return "fr";
  if (browser.startsWith("de")) return "de";
  if (browser.startsWith("es")) return "es";
  return "en";
}

function getInitialView() {
  const url = new URL(window.location.href);
  const requested = url.searchParams.get("view");
  if (["home", "privacy", "support"].includes(requested)) return requested;
  const fromBody = document.body.dataset.initialView;
  if (["home", "privacy", "support"].includes(fromBody)) return fromBody;
  const file = url.pathname.split("/").pop();
  if (file === "privacy.html") return "privacy";
  if (file === "support.html") return "support";
  return "home";
}

function getHtmlLang(language) {
  if (language === "zhHans") return "zh-Hans";
  if (language === "zhHant") return "zh-Hant";
  return language;
}

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function setText(selector, value) {
  document.querySelectorAll(selector).forEach((element) => {
    element.textContent = value;
  });
}

function renderSectionList(sections) {
  return `
    <div class="section-list">
      ${sections.map(([title, body]) => `
        <section class="paper-section">
          <h2>${escapeHtml(title)}</h2>
          <p>${escapeHtml(body)}</p>
        </section>
      `).join("")}
    </div>
  `;
}

function getViewCopy(view, language) {
  const base = translations[language] || translations.en;
  const home = homeCopy[language] || homeCopy.en;
  if (view === "privacy") {
    return {
      eyebrow: "POLARPOP",
      title: base.privacyTitle,
      lead: base.privacyLead,
      meta: "",
      sections: base.privacySections,
      titleSuffix: base.privacy
    };
  }
  if (view === "support") {
    return {
      eyebrow: "POLARPOP",
      title: base.supportTitle,
      lead: base.supportLead,
      meta: "",
      sections: base.supportSections,
      titleSuffix: base.support
    };
  }
  return {
    eyebrow: home.eyebrow,
    title: home.title,
    lead: home.lead,
    meta: "",
    sections: home.sections,
    titleSuffix: "PolarPop"
  };
}

function renderPaper(animate = true) {
  const paper = document.getElementById("paperSheet");
  const content = document.getElementById("paperContent");
  if (!paper || !content) return;

  const copy = getViewCopy(currentView, currentLanguage);
  
  if (currentView === "home") {
    content.innerHTML = `
      <div class="home-grid">
        <header class="paper-heading">
          <div class="paper-eyebrow">${escapeHtml(copy.eyebrow)}</div>
          <h1>${escapeHtml(copy.title)}</h1>
          <p>${escapeHtml(copy.lead)}</p>
        </header>
        <div class="home-main">
          ${renderSectionList(copy.sections)}
        </div>
        <div class="home-aside">
          <div class="paper-hero">
            <img src="assets/hero-${currentLanguage}.png" alt="PolarPop Showcase">
          </div>
        </div>
      </div>
    `;
  } else {
    content.innerHTML = `
      <header class="paper-heading">
        <div class="paper-eyebrow">${escapeHtml(copy.eyebrow)}</div>
        <h1>${escapeHtml(copy.title)}</h1>
        <p>${escapeHtml(copy.lead)}</p>
        ${copy.meta ? `<p class="meta">${escapeHtml(copy.meta)}</p>` : ""}
      </header>
      ${renderSectionList(copy.sections)}
    `;
  }

  document.title = currentView === "home" ? "PolarPop" : `PolarPop - ${copy.titleSuffix}`;
  updateActiveNav();

  if (animate) {
    paper.classList.remove("is-printing");
    void paper.offsetWidth;
    paper.classList.add("is-printing");
  }
}

function updateActiveNav() {
  document.querySelectorAll("[data-view-link]").forEach((link) => {
    link.classList.toggle("is-active", link.dataset.viewLink === currentView);
  });
}

function pageForView(view) {
  if (view === "privacy") return "privacy.html";
  if (view === "support") return "support.html";
  return "index.html";
}

function updateNavLinks() {
  document.querySelectorAll("[data-view-link]").forEach((link) => {
    const view = link.dataset.viewLink;
    const url = new URL(pageForView(view), window.location.href);
    url.searchParams.set("lang", currentLanguage);
    link.setAttribute("href", `${url.pathname.split("/").pop()}${url.search}`);
  });
}

function switchView(view, pushState = true) {
  currentView = ["home", "privacy", "support"].includes(view) ? view : "home";
  renderPaper(true);
  updateNavLinks();
  if (pushState) {
    const url = new URL(pageForView(currentView), window.location.href);
    url.searchParams.set("lang", currentLanguage);
    window.history.pushState({ view: currentView, language: currentLanguage }, "", `${url.pathname.split("/").pop()}${url.search}`);
  }
}

function setLanguage(language, animate = false) {
  currentLanguage = translations[language] ? language : "en";
  const copy = translations[currentLanguage] || translations.en;
  const home = homeCopy[currentLanguage] || homeCopy.en;
  document.documentElement.lang = getHtmlLang(currentLanguage);
  localStorage.setItem("polarpop-language", currentLanguage);
  setText("[data-copy='homeNav']", home.nav);
  setText("[data-copy='privacyNav']", copy.privacy);
  setText("[data-copy='supportNav']", copy.support);
  const select = document.getElementById("languageSelect");
  if (select) select.value = currentLanguage;
  updateNavLinks();
  renderPaper(animate);
}

function initLanguageSelect() {
  const select = document.getElementById("languageSelect");
  if (!select) return;
  select.innerHTML = languageOrder.map((language) => {
    const copy = translations[language];
    return `<option value="${language}">${escapeHtml(copy.langName)}</option>`;
  }).join("");
  select.addEventListener("change", () => {
    setLanguage(select.value, true);
    const url = new URL(pageForView(currentView), window.location.href);
    url.searchParams.set("lang", currentLanguage);
    window.history.replaceState({ view: currentView, language: currentLanguage }, "", `${url.pathname.split("/").pop()}${url.search}`);
  });
}

function initViewLinks() {
  document.querySelectorAll("[data-view-link]").forEach((link) => {
    link.addEventListener("click", (event) => {
      event.preventDefault();
      switchView(link.dataset.viewLink, true);
    });
  });
}

window.addEventListener("popstate", () => {
  currentView = getInitialView();
  currentLanguage = getInitialLanguage();
  setLanguage(currentLanguage, true);
});

document.addEventListener("DOMContentLoaded", () => {
  currentView = getInitialView();
  currentLanguage = getInitialLanguage();
  initLanguageSelect();
  initViewLinks();
  setLanguage(currentLanguage, true);
});
